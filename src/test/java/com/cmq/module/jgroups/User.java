package com.cmq.module.jgroups;

public class User {

	private String code;
	private String name;
	
	public User() {
		super();
	}
	public User(String code, String name) {
		super();
		this.code = code;
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public String getName() {
		return name;
	}
	
	
}
